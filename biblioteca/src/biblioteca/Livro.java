package biblioteca;

public class Livro {

    public String titulo, id, autor, isbn, categoria;
    public int edicao;



    public String getTitulo() {
        return titulo;
    }

    public String getId() {
        return id;
    }

    public String getAutor() {
        return autor;
    }

    public String getIsbn() {
        return isbn;
    }

    public String getCategoria() {
        return categoria;
    }

    public int getEdicao() {
        return edicao;
    }

}
